                <div class="program-card">
                    <h4 class="program-title"><i class="fas fa-file-medical me-2"></i>New Guidelines for Foreign Medical Study (Post 18-Nov-2021)</h4>
                    <p class="text-white">No foreign medical graduate shall be granted permanent registration in India unless they have:</p>
                    <ul class="program-list">
                        <li>
                            <i class="fas fa-check-circle"></i>
                            <div>
                                <strong>NEET Qualification</strong><br>
                                Qualified NEET (before taking admission in medical college)
                            </div>
                        </li>
                        <li>
                            <i class="fas fa-check-circle"></i>
                            <div>
                                <strong>Course Duration</strong><br>
                                Undergone a course leading to foreign medical degree with minimum duration of fifty-four months
                            </div>
                        </li>
                        <li>
                            <i class="fas fa-check-circle"></i>
                            <div>
                                <strong>Internship</strong><br>
                                Undergone an internship for a minimum duration of twelve months in the same foreign medical institution
                            </div>
                        </li>
                        <li>
                            <i class="fas fa-check-circle"></i>
                            <div>
                                <strong>Medium of Instruction</strong><br>
                                Received a foreign medical degree with medium of instructions in English
                            </div>
                        </li>
                        <li>
                            <i class="fas fa-check-circle"></i>
                            <div>
                                <strong>Registration</strong><br>
                                Registered with the respective professional regulatory body or otherwise, competent to grant license to practice medicine in their respective jurisdiction
                            </div>
                        </li>
                        <li>
                            <i class="fas fa-check-circle"></i>
                            <div>
                                <strong>Indian Internship</strong><br>
                                Undergone supervised internship in India for a minimum term of twelve months
                            </div>
                        </li>
                        <li>
                            <i class="fas fa-check-circle"></i>
                            <div>
                                <strong>Exit Test</strong><br>
                                Cleared the National Exit Test or such other mandated test conducted by the commission
                            </div>
                        </li>
                        <li>
                            <i class="fas fa-check-circle"></i>
                            <div>
                                <strong>Course Completion</strong><br>
                                The total duration of the foreign medical graduation course must be completed within ten years from the date of joining
                            </div>
                        </li>
                    </ul>
